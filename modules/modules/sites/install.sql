-- --------------------------------------------------------
-- Таблица настроек

DROP TABLE IF EXISTS `bff_config`;   
CREATE TABLE `bff_config` (
    `config_name` varchar(255) DEFAULT '' NOT NULL,
    `config_value` text DEFAULT '' NOT NULL,
    `is_dynamic` tinyint(1) unsigned NOT NULL default 0,
    PRIMARY KEY (`config_name`),
    KEY `is_dynamic` (`is_dynamic`)  
)  ENGINE=MyISAM DEFAULT CHARSET=utf8;                                                            

-- --------------------------------------------------------
-- Структура таблицы `bff_counters`

DROP TABLE IF EXISTS `bff_counters`;
CREATE TABLE `bff_counters` (
  `id` smallint(5) unsigned NOT NULL auto_increment,
  `num` smallint(5) unsigned NOT NULL default 0,
  `title` varchar(100) NOT NULL,
  `code` text NOT NULL,
  `enabled` tinyint(1) unsigned NOT NULL default 1,
  `created` timestamp NOT NULL default '0000-00-00 00:00:00',
  PRIMARY KEY  (`id`)
) ENGINE=MyISAM DEFAULT CHARSET=utf8 AUTO_INCREMENT=1 ;

-- --------------------------------------------------------
-- таблица подписавшихся на рассылку

DROP TABLE IF EXISTS `bff_subscribes`;
CREATE TABLE `bff_subscribes` (
  `id` int(11) unsigned NOT NULL auto_increment,
  `email` varchar(50) NOT NULL,
  `created` timestamp NOT NULL default '0000-00-00 00:00:00',
  PRIMARY KEY  (`id`),
  UNIQUE u1 (`email`)
) ENGINE=MyISAM  DEFAULT CHARSET=utf8 AUTO_INCREMENT=1 ;

